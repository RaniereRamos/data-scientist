# Gerando um dataframe de datas / Creating a data dataframe
sono <- data.frame(bed.time = ymd_hms("2013-09-01 23:05:24",
        "2013-09-02 22:51:09", "2013-09-04 00:09:16", "2013-09-04 23:43:31",
        "2013-09-02 08:03:29", "2013-09-03 07:34:21", "2013-09-08 00:22:27"),
        rise.time = ymd_hms("2013-09-02 08:03:29", "2013-09-03 07:34:21",
        "2013-09-04 07:45:06", "2013-09-05 07:07:17", "2013-09-06 08:17:13",
        "2013-09-07 06:52:11", "2013-09-08 07:15:19"),
        sleep.time = dhours(c(6.74, 7.92, 7.01, 6.23, 6.34, 7.42, 6.45)))

sono
